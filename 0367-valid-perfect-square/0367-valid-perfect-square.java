class Solution {
    public boolean isPerfectSquare(int num) {
        if(num == 1){
            return true;
        }
        long l = 1 , r = num ;
        while(l <= r){
            long mid = l + (r-l)/2 ;
            long val = mid * mid; 
            if(val == num){
                return true;
            }else if (val > num){
                r = mid - 1;
            }else{
                l = mid + 1;
            }
        }
        return false;
    }
}